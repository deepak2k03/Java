public class type_conversion_and_type_casting {
    public static void main(String[] args) {
        byte x=5;
        int b=12;
        //explicit casting -- explicit conversion of datatype
        int c=(byte)b;
        //implicit casting -- implicit conversion of datatype
        int d=x;

        System.out.println(c);
        System.out.println(d);



    }
}
